					<h1>En quoi consiste ce projet?</h1>
					<h2></h2>
					<p>Ce projet a été réalisé dans le cadre de la troisième année d’étude en école d’ingénieurs à UniLaSalle-Amiens. Ce projet nous permet de mettre en application ce que nous avons appris au cours de notre cursus scolaire, ainsi que d’approfondir nos connaissances. Nous sommes trois étudiants et nous avons choisi pour ce projet le thème de la surveillance environnementale.   </p>
					 <p>Durant ces deux dernières années de pandémie, le COVID-19 a forcé la population à renforcer les contrôles de maitrise de qualité de l’air. De plus, les lieux clos ont eu besoin de gérer le flux de personnes dans leur bâtiment afin de limiter la transmission.  </p>
					
					<p>
						Cependant depuis la fin des années 2000, le développement des objets connectés et de l’IoT n’a cessé de se développer. En effet, l’IoT ou Internet des objets est un terme utilisé pour la première fois par Kevin ASHTON en 1999 et désigne l’infrastructure interconnectant différents des objets physiques ou virtuelles à internet permettant ainsi d’obtenir des informations diverses. De nos jours, on dénombre plus d’objets connectés à internet que d’humains.  </p>

					<p><b></b>
						C’est pour cela que nous avons créé un boitier muni de capteurs permettant de mesurer la température, la luminosité, le taux d’humidité, le gaz dans l’air et la qualité sonore. Ces capteurs sont branchés sur une carte Arduino Mega connectée grâce à un câble Ethernet au réseau ce qui lui permet de mettre à jour en direct une base de données consultable en ligne sur le site WEB ou sur l’application de notre projet.  </p>


					<p>
					Nous avons également conçu une autre boitier plus petite munie de deux capteurs de distance et relié à une carte ESP32 connecté par Wi-Fi. Ces informations sont également consultables en ligne.</p>

					<p>
					Ce sujet de projet a déjà été réalisé l’année précédente mais n’a pas pu être abouti. En effet, le groupe a réussi à concevoir un boitier contenant une carte Arduino et des capteurs relié à une base de données mais le site n’a pas pu être fini et la partie détection de présence n’a pas pu être traitée. Grâce à notre tutrice nous avons pu récupérer le boitier utilisé par le groupe de l’année précédente, cependant nous n’avions accès ni au code ni au site créé auparavant. </p>

